function selecionarFrases(num){
    var frase = document.getElementById('frase');
    var posicaoAtual = document.getElementById('posicaoAtual').getAttribute("name");

    posicaoAtual = Number(posicaoAtual) + Number(num); 

    if(posicaoAtual > 9){
        posicaoAtual = 0;
    }

    if(posicaoAtual < 0){
        posicaoAtual = 9;
    }
 
    if(posicaoAtual == 0){
        frase.innerText = 'O universo não seria grande coisa se não fosse o lar das pessoas que você ama.'
    }
    if(posicaoAtual == 1){
        frase.innerText = 'Inteligência é a capacidade de se adaptar à mudança.'
    }
    if(posicaoAtual == 2){
        frase.innerText = 'Agressão, por parte dos humanos, pode destruí-los.'
    }
    if(posicaoAtual == 3){
        frase.innerText = 'Eu sou apenas uma criança que nunca cresceu. Eu ainda continuo fazendo aquelas perguntas "como" e "por quê".'
    }
    if(posicaoAtual == 4){
        frase.innerText = 'Embora eu não possa me mover e tenha que falar através de um computador, em minha mente eu sou livre.'
    }
    if(posicaoAtual == 5){
        frase.innerText = 'A vida seria trágica se não fosse engraçada.'
    }
    if(posicaoAtual == 6){
        frase.innerText = 'Meu objetivo é simples: é a compreensão completa do universo, por que ele é como é e por que ele existe.'
    }
    if(posicaoAtual == 7){
        frase.innerText = 'A ciência pode responder o "como", mas não o "porquê".'
    }
    if(posicaoAtual == 8){
        frase.innerText = 'O maior inimigo do conhecimento não é a ignorância, mas a ilusão do conhecimento.'
    }
    if(posicaoAtual == 9){
        frase.innerText = 'A humanidade precisa continuar no espaço para sua sobrevivência a longo prazo.'
    }

    document.getElementById('posicaoAtual').setAttribute("name",posicaoAtual);

}

